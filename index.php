    <h1><i class="fa fa-info-circle icon"></i> About</h1>
    <p>INCOME is the principal international event in the field of mechanochemistry and mechanical alloying, held once in every three years. Ninth in the series of these truly international conferences, <span class="in">I</span><span class="in">N</span><span class="co">C</span><span class="co">O</span><span class="me">M</span><span class="me">E</span>2017 will focus on the latest development in the field. The conference is intended to offer a premier multidisciplinary forum for the delegates from all over the world to share their findings, and exchange cutting edge ideas and experience in all aspects of mechanochemistry and mechanical alloying in an essentially non-formal environment. <span class="in">I</span><span class="in">N</span><span class="co">C</span><span class="co">O</span><span class="me">M</span><span class="me">E</span>2017 is intended to promote contact between basic research and technological needs for industrial applications. The conference will also provide a platform for the researchers to find global partners for future collaborations. The proceedings of the conference will be published in the esteemed journals. <span class="in">I</span><span class="in">N</span><span class="co">C</span><span class="co">O</span><span class="me">M</span><span class="me">E</span>2017 is organized by the International Mechanochemical Association (IMA) under IUPAC and the Institute of Geotechnics of the Slovak Academy of Sciences.</p>
