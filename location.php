      <h1><i class="fa fa-institution icon"></i> Venue</h1>
      <p>Try something new and undiscovered. A place where east and west meet, where history and human destiny mix with the present. Join us to discover the City of Košice – a unique Central European metropolis and a true treasure among Slovak cities!</p>
      <p>Košice is home to the oldest marathon in Europe, it boasts the largest heritage reservation in Slovakia, as well as the largest and most important gold treasure in Europe, but most importantly, it is a city where life is lived and enjoyed.</p>
      <p>So come and sample a tasty coffee or the cocktail Košické zlato (Košice gold) at one of the street cafes on a Friday evening, when the people of Košice take to the streets and bars of the Old Town center, or why not visit the Východoslovenské Múzeum (East Slovak Museum) and admire the gold treasure (and secretly wish that you had discovered it), soak up the tropical atmosphere of the Botanical Gardens or just enjoy a stroll around the old streets of the city and drop into a restaurant or bar for a glass of excellent Slovak wine or some local delicacy.</p>
      <p class="w3-right">* <a href="http://www.visitkosice.eu/en/" target="_blank">visitkosice.eu</a></p>
